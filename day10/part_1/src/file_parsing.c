#include "pipe_maze.h"

void	parse_values(int fd, char map[140][142])
{
	int		i;

	i = 0;
	while (read(fd, &map[i], 141) != 0)
	{
		map[i][141] = '\0';
		i++;
	}
	check_maze(map);
}
